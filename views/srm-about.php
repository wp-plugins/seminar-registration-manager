    <div class="srm-content">
        Thank you for checking out Seminar Registration Manager. This plugin allows you to have visitors to your site register for seminars, and complete the payment on your site. You'll want to be sure that you have an SSL certificate enabled for the site, and be sure all registration pages are secure. There is a <a href="http://wordpress.org/extend/plugins/wordpress-https/" target="_blank">good plugin</a> for this available.
        <p>
            I don't have complete documentation or anything yet, but check out what I do have, and then hopefully you can find your way around from there.
        </p>
        <h2>Documentation</h2>
        <h3>Basic Usage</h3>
        <ul>
            <li>Visit the SRM settings page and make sure that your payment gateway is chosen, and that anything you need to change has been updated.</li>
            <li>Add a seminar</li>
            <li>Create any coupon codes you'd like to use.</li>
            <li>Place the shorcode <strong>[srm_register]</strong> on a page or post where you would like the registration to occur (by default a page is created for you for this purpose).</li>
        </ul>
        <h2>Planned Updates</h2>
        The following are things I'd like to add to the SRM Plugin in the future:
        <ul>
            <li>Additional payment gateways</li>
            <li>Ability to toggle additional registrants feature on or off</li>
            <li>Google Maps integration</li>
            <li>International support (currently only the U.S. is supported)</li>
        </ul>
